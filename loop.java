public class loop {
    public static void main(String[] args) {
        // using while loop
        int count = 0;
        while (count <= 4) {
            System.out.println("Aditya Upaganlawar");
            count++;
        }
        // using for loop
        String str = "Aditya Upaganlawar";
        for (int i = 0; i <= 4; i++) {
            System.out.println(str);
        }
        // using while loop

        int num = 1;
        while (num <= 10) {
            System.out.println(num + " ");
            num++;
        }
        // using for loop

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

    }
}